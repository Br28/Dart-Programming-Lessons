void main() {
  // var The var keyword can hold any kind of datatype after intialization
  var name = 100;

  // final The final keyword holds a constant value at run time
  final grade = 70;

  // Const The const keyword holds a constant value at compile time
  const height = 1.72;

  // dynamic The dynamic keyword can hold a value that can be changed at any time
  dynamic range = 1;
  range = "so far";
}
